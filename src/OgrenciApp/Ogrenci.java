package OgrenciApp;

public class Ogrenci {
	private String ad;
	private String soyAd;
	private int ogrNo;

	public String getAd() {
		return ad;
	}

	public String getSoyAd() {
		return soyAd;
	}

	public int getOgrNo() {
		return ogrNo;
	}

	public void setAd(String ad) {
		this.ad = ad;
	}

	public void setSoyAd(String soyAd) {
		this.soyAd = soyAd;
	}

	public void setOgrNo(int ogrNo) {
		this.ogrNo = ogrNo;
	}

}
